#!/usr/bin/env node
/**
 * 根据 MVP_SERVER_IP 生成 editor/renderer 的 .env.production（无 Nginx 试运行用 :3002）
 * 用法：MVP_SERVER_IP=192.168.1.100 node scripts/prepare-trial-env.mjs
 */
import { writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const ip = (process.env.MVP_SERVER_IP || '192.168.1.100').trim()
const apiPort = (process.env.MVP_API_PORT || '3002').trim()
const base = `http://${ip}:${apiPort}`

const editorEnv = `# generated by scripts/prepare-trial-env.mjs — do not commit if customized
VITE_API_BASE=${base}
VITE_PAGE_POLICY=strict
`

const rendererEnv = `# generated by scripts/prepare-trial-env.mjs
VITE_API_BASE=${base}
`

writeFileSync(join(root, 'packages/editor/.env.production'), editorEnv, 'utf8')
writeFileSync(join(root, 'packages/renderer/.env.production'), rendererEnv, 'utf8')

console.log(`Wrote .env.production with VITE_API_BASE=${base}`)
